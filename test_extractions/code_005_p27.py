# 0.4.5 條件與判斷 Condition and Decision Making

x = 10

if x > 5:
    print("x is greater than 5")
else:
    print("x is less than or equal to 5")

# 多重條件判斷
score = 85

if score >= 90:
    grade = "A"
elif score >= 80:
    grade = "B"
elif score >= 70:
    grade = "C"
else:
    grade = "D"

print(grade)  # B
