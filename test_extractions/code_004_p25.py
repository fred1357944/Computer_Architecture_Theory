# 0.4.2 變數與算數運算 Variable and Arithmetic Operators
# 頁碼：25

x = 10

print(x+4)    # 14
print(x-1)    # 9
print(x*2)    # 20
print(x/4)    # 2.5
print(x**2)   # 100
print(x%3)    # 1
print(x//3)   # 3

# Line Description:
# 1  創建一個名為 x 的變數，將變數 x 指定到數值 10
# 3  輸出 x+4 後的數值，10+4=14，輸出值為 14
# 4  輸出 x-1 後的數值，10-1=9，輸出值為 9
# 5  輸出 x*2 後的數值，10*2=20，輸出值為 20
# 6  輸出 x/4 後的數值，10/4=2.5，輸出值為 2.5
# 7  輸出 x**2 後的數值，10*10=100，輸出值為 100
# 8  輸出 x%3 後的數值，10/3=3 餘 1，輸出值為 1
# 9  輸出 x//3 後的數值，10/3=3 餘 1，輸出值為 3

