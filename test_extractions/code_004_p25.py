# 0.4.3 變數遞增與遞減運算 Increment and Decrement
# 頁碼：26

x = 10

x = x + 5
print(x)  # 15

# Line Description:
# 1  創建一個名為 x 的變數，將 x 指定到數值 10
# 3  將 x+5 後的數值，即 15，將變數 x 指定到數值 15
# 4  輸出 x 的數值

# 在程式運算中，x=x+a 也可以寫成 x+=a，即上一個案例可改寫成：

x = 10

x += 5
print(x)  # 15

# 此兩種寫法的運算結果是相同的，若是要將變數遞減，亦可將 x=x-a 寫成 x-=a